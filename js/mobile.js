function Mobile() {
  document.getElementsByClassName("mobile_header")[0].style.display = "none";
  document.getElementsByClassName("main_mobile_header")[0].style.display =
    "block";
}
function Exit() {
  document.getElementsByClassName("mobile_header")[0].style.display = "block";
  document.getElementsByClassName("main_mobile_header")[0].style.display =
    "none";
}
